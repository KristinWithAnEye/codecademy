// Declare myAge variable
const myAge = 33;

// Declare earlyYears variable and reassign it
let earlyYears = 2;
earlyYears *= 10.5;

// Declare laterYears variable
let laterYears = myAge - 2;

// Multiply laterYears by 4
laterYears *= 4;

// Declare myAgeInDogYears variable
const myAgeInDogYears = earlyYears + laterYears;

// Declare myName variable
const myName = "Kristin".toLowerCase();

// Log human years vs. dog years
console.log(`My name is ${myName}. I am ${myAge} years old in human years, which is ${myAgeInDogYears} years old in dog years.`);